    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String codetrp = cari_transaksi.getText();
        String ket = alasan.getText();
        try {
            if (!ket.isEmpty() || !codetrp.isEmpty() || !codetrp.isBlank()) {
                if (Integer.parseInt(totalobatp.getText()) == 0 || Integer.parseInt(totalhargap.getText()) == 0) {
                    JOptionPane.showMessageDialog(this, "Tidak Bisa Melakukan Pengemabalian Karna Tidak Barang Yang Di kembalikan");
                } else {
                    java.sql.Connection connection = DB.getConnection();
                    SimpleDateFormat simpleDateFormat = new SimpleDateFormat("ddMMyykkmmss");
                    String date2 = simpleDateFormat.format(new Date());
                    String codeTRX = "RTRXP_" + date2;
                    String insertDetailQuery = "INSERT INTO detail_retur_pembelian (kode_retur_pembelian,no_batch,qty _sebelum_retur,qty_retur,selisih,harga,subtotal) VALUES (?,?,?,?,?,?,?)";
                    PreparedStatement insertDetailStatement = connection.prepareStatement(insertDetailQuery);

                    for (int i = 0; i < jTable1.getRowCount(); i++) {
                        insertDetailStatement.setString(1, codeTRX);
                        insertDetailStatement.setString(2, jTable1.getValueAt(i, 0).toString());
                        insertDetailStatement.setString(3, jTable1.getValueAt(i, 3).toString());
                        insertDetailStatement.setString(4, jTable1.getValueAt(i, 4).toString());
                        insertDetailStatement.setString(5, jTable1.getValueAt(i, 5).toString());
                        insertDetailStatement.setString(6, jTable1.getValueAt(i, 6).toString());
                        insertDetailStatement.setString(7, jTable1.getValueAt(i, 7).toString());

                    }
                    String insertRTransaksiQuery = "INSERT INTO retur_pembelian (kode_retur_pembelian,kode_transaksi_pembelian,id_user,kode_supplier,jumlah_retur ,alasan_retur ,tanggal_retur ) VALUES (?,?,?,?,?)";
                    PreparedStatement insertTransaksiStatement = connection.prepareStatement(insertRTransaksiQuery);
                    insertTransaksiStatement.setString(1, codetrp);
                    insertTransaksiStatement.setString(2, codeTRX);
                    insertTransaksiStatement.setString(3, idusrr);
                    insertTransaksiStatement.setString(4, txt_nama.getText());
                    insertTransaksiStatement.setString(5, totalobatp.getText());
                    insertTransaksiStatement.setString(6, ket);
                    insertTransaksiStatement.addBatch();

                }
            } else {
                JOptionPane.showMessageDialog(this, "Semua Kolom Harus Diisi.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReturPembelianView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

